    /**
     * valida todas las entradas y si son correctas, da de alta al restaurante
     * @param evt 
     */
    private void jButtonHechoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHechoActionPerformed
        String nombre = jTextField1.getText();
        String cif = jTextField2.getText();
        String calle = jTextField3.getText();
        String numero = jTextField4.getText();
        String ciudad= jTextField5.getText();
        String cp= jTextField6.getText();
       String especstr = (String )comboBoxEspecialidades.getSelectedItem();
       Restaurante.especialidades espec = Restaurante.especialidades.valueOf(especstr);
        boolean tieneCatering = jCheckBoxTieneCatering.isSelected();
        int tEnvio = (int)jSpinnerTiempoEnvio.getValue();
        double gEnvio = (double) jSpinnerGastoEnvio.getValue();
        
        String error="";
        if (nombre.isBlank()|| cif.isBlank()||calle.isBlank()||ciudad.isBlank()
                ||cp.isBlank() )error+="todos los campos son obligatorios";
        if (cp.length()!=5) error+="el codigo postal debe tener 5 numeros";
        else {
            try {
                Integer.parseInt(cp);
            }catch (NumberFormatException nfe){
                error += "el codigo postal debe ser numérico";
            }
        }try {
                Integer.parseInt( numero);
            }catch (NumberFormatException nfe){
                error += "el numero de la calle debe ser numérico";
        }if (!error.isBlank())JOptionPane.showMessageDialog(this, error,"Error", JOptionPane.ERROR_MESSAGE);
        else{
            Direccion dir = new Direccion (calle, numero, ciudad, cp);
            if (!tieneCatering){
                if (!Sistema.darDeAltaRestaurante(nombre, cif, dir, espec, tEnvio, gEnvio, comidas, null, tieneCatering)){
                if (Sistema.getRestaurantes().containsKey(cif)) JOptionPane.showMessageDialog(this, "ya existe un restaurante registrado con ese cif", "Error", JOptionPane.ERROR_MESSAGE);
                }
                else {JOptionPane.showMessageDialog(this, "Restaurante creado con éxito");}
            }
            else{
                if (!Sistema.darDeAltaRestaurante(nombre, cif, dir, espec, tEnvio, gEnvio, comidas, catering, tieneCatering)){
                    if (Sistema.getRestaurantes().containsKey(cif)) JOptionPane.showMessageDialog(this, "ya existe un restaurante registrado con ese cif", "Error", JOptionPane.ERROR_MESSAGE);
                }
                else {
                    if (getParent()instanceof JAdmin parent){
                        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) parent.getCbRestaurantes().getModel();
                        model.addElement(nombre);
                        parent.getCbRestaurantes().setModel(model);
                        }
                    JOptionPane.showMessageDialog(this, "Restaurante creado con éxito");
                }
            }
        }
    }//GEN-LAST:event_jButtonHechoActionPerformed

