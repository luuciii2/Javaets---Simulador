    /**
     * visibiliza opciones para configurar el catering del Restaurante,
     * las comidas del catering son una seleccion de las comidas del restaurante
     * @param evt 
     */
    private void jCheckBoxTieneCateringActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxTieneCateringActionPerformed
        
        if (jCheckBoxTieneCatering.isSelected()){
            if (comidas.isEmpty()){
                JOptionPane.showMessageDialog(this,"Para crear un catering primero debe añadir comidas al restaurante" );
                jCheckBoxTieneCatering.setSelected(false);
            }
            else new JCrearCatering(this, true, comidas).setVisible(true);
        }
        else {
            int resp = JOptionPane.showConfirmDialog(this, "¿Está seguro de que quiere eliminar el catering?", "Eliminar catering", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            //0 -sí
            //1-no
            if (resp==0)catering = null;
            else jCheckBoxTieneCatering.setSelected(true);
        }
    }//GEN-LAST:event_jCheckBoxTieneCateringActionPerformed

