    /**
     * Crea la ventana JRestaurante y añade los paneles de la comida
     * @param parent
     * @param modal
     * @param res
     */
    public JRestaurante(javax.swing.JDialog parent, boolean modal, Restaurante res) {
        super(parent, modal);
        this.res= res;
        initComponents();
        Sistema.centrarContainer(this);
        for (Comida com: res.getComidas()){
            PanelComida panelcom = new PanelComida(com);
            panelcom.setSize(495, 145);
            jPanel1.add(panelcom);
            panelcom.getSpCantidad().setModel(new javax.swing.SpinnerNumberModel(0, 0, null, 1));

            panelcom.getSpCantidad().addChangeListener(new ChangeListener() {
                @Override
                /**
                 * añade o quita la comida del ArrayList de las comidas del catering
                 * Lo hace cada vez que se presiona el spCantidad
                 */
                public void stateChanged(javax.swing.event.ChangeEvent evt) {
                    int veces = (int)comidasSeleccionadas.stream().filter(t->t.equals(com)).count();
                    if ((int)panelcom.getSpCantidad().getValue()<veces) comidasSeleccionadas.remove(com);
                    else  comidasSeleccionadas.add(com);
                    
                }
            });
        }
    }
    

