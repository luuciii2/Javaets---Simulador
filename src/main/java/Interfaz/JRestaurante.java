    /**
     * comprueba que haya comidas seleccionadas y 
     * en tal caso abre una ventana Pedido asociada al pedido realizado
     * @param evt 
     */
    private void termPedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_termPedActionPerformed
    if (!comidasSeleccionadas.isEmpty()){
            DialogPrincipal principal = (DialogPrincipal) JRestaurante.this.getParent();
            Logica.Pedido pedido = new Logica.Pedido(principal.getCliente(),res,comidasSeleccionadas);
            
            new JPedido(JRestaurante.this , true, pedido);
            }
            else JOptionPane.showMessageDialog(JRestaurante.this, "No hay comidas elegidas", "ERROR AL HACER EL PEDIDO", JOptionPane.ERROR_MESSAGE);
            }//GEN-LAST:event_termPedActionPerformed

