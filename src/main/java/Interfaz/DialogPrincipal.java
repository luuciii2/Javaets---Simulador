    /**
     * si el texto introducido coindice con algun nombre o cif de los restaurantes dados de alta,
     * pone en visible un botón que al pinchar te lleva a su ventana Restaurante
     * @param evt 
     */
    
    private void jButtonLupaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLupaActionPerformed
        String busqueda = TextFieldBusqueda.getText();
        boolean existe = false;
        Restaurante res = null;
        if (Sistema.getRestaurantes().containsKey(busqueda)){//si lo busca por cif
            res = Sistema.getRestaurantes().get(busqueda);
            existe = true;
                    }
        else{//si lo busca por nombre
            for (Restaurante rest: Sistema.restaurantesArrayList()){
                if (rest.getNombre().equals(busqueda)){
                    existe = true;
                    res= rest;
                    break;
                }
            }
        }
        if (!existe){
            JOptionPane.showMessageDialog(this, "No existe ese restaurante", "Not Found", JOptionPane.ERROR_MESSAGE);
        }
        else {
            try{
            final JDialog  parent = this;
            final Restaurante rest = res;
            jButtonEncontrado.setVisible(true);
            jButtonEncontrado.addActionListener((ActionEvent e) -> {
                new JRestaurante(parent, true,rest).setVisible(true);
                parent.setVisible(false);
            });
            }catch(NullPointerException npe){
                System.out.println(npe.getMessage());
            }
        }
        
            
                
           
            
        
    }//GEN-LAST:event_jButtonLupaActionPerformed
/**
 * ordena  filtra los restaurantes del combobox segun el criteri seleccionado
 * @param evt 
 */
    private void comboBoxOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxOrdenarActionPerformed
        String eleccion =(String) comboBoxOrdenar.getSelectedItem();
        ArrayList<Restaurante> restaurantes= new ArrayList<>();
        switch (eleccion){
            case ("por cercanía")->{
                restaurantes = Sistema.buscarCercanos(cliente);
            }
            case ("relevancia")-> {
                restaurantes = Sistema.ordenarRelevancia(Sistema.buscarCercanos(cliente));
            }case("tiempo de envío")->{
                restaurantes = Sistema.ordenarTiempoEnvio(Sistema.buscarCercanos(cliente));
            }case("solo Catering")->{
                restaurantes = Sistema.filtrarCatering(Sistema.buscarCercanos(cliente));
            }case ("especialidad")->{
                int espec = JOptionPane.showOptionDialog(this,
                        "selecciona especialidad",
                        "ESPECIALIDAD",
                        JOptionPane.DEFAULT_OPTION, 
                        JOptionPane.PLAIN_MESSAGE, 
                        null,
                        Restaurante.especialidades.values(),
                        Restaurante.especialidades.CHINO);
                try{
                restaurantes = Sistema.filtrarEspecialidad(Sistema.buscarCercanos(cliente), Restaurante.especialidades.values()[espec]);}
                catch(ArrayIndexOutOfBoundsException e){
                    restaurantes = Sistema.buscarCercanos(cliente);
                }
            }
        }
            tablaRes.setModel(new javax.swing.table.DefaultTableModel(
    obtenerResumen(restaurantes),
         new String [] {
           "Restaurante", "Especialidad", "Calificación"
    }
));        
    }//GEN-LAST:event_comboBoxOrdenarActionPerformed

