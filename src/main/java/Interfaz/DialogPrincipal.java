    /**
     * crea una matriz necesaria para reflejar los datos de los restaurante en una tabla
     * @param restaurantes
     * @return String[][]  
     */
    public String [] [] obtenerResumen(ArrayList<Restaurante> restaurantes){
          String [][] resumen = new String[restaurantes.size()][3];
          int i =0;
          for (Restaurante res : restaurantes){
              resumen [i][0] = res.getNombre();
              resumen [i][1] = res.getEspecialidad().name();
              if (res.getNumCalificaciones()!=0){
                  double nota10= Math.round(res.calcularCalificacion()*10);
              resumen [i][2] = String.valueOf(nota10/10);
              }
              else resumen[i][2] = "Sin calificar";
              i++;
          }
         return resumen;   
    }

